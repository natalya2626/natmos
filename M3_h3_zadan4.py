# Задание 4
# Пользователь вводит любое целое число. Необходимо из этого
# целого числа удалить все цифры 3 и 6 и
# вывести обратно  на экран
    
n = input().strip()

# Удаляем все '3' и '6' из строки
result = ''.join(c for c in n if c not in '36')

# Если результат пустой или остался только минус — выводим 0
if result == '' or result == '-':
    print(0)
else:
    # Преобразуем в int и обратно, чтобы корректно обработать ведущие нули и -0
    print(int(result))