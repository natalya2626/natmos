# Задание 3
# Запрограммируйте класс Money (объект класса оперирует одной валютой)
#  для работы с деньгами. В классе должны быть предусмотрены поле для 
# хранения целой части денег (доллары, евро, гривны и т.д.) и
# поле для хранения копеек (центы, евроценты, копейки  и т.д.).
# Реализовать методы для вывода суммы на экран, задания значений для частей.

class Money:
    def __init__(self, whole, cents, currency):                 # конструктор init. Это метод, 
                                                                # который вызывается 
                                                                # при создании нового объекта класса Money.
        self._whole = whole                               # он принимает целую часть доллара
        self._cents = cents                               # поле для хранения центов
        self._currency = currency                         # (где currency — строка: "USD", "EUR", "UAH").

    @property                                   # свойства
    def whole(self):                            # Создаётся свойство только для чтения целой части валюты
        return self._whole         
    
    @property                                   # свойства
    def cents(self):                            # Создаётся свойство только для чтения центов копеек
        return self._cents         
    
    @property                                   # свойства
    def currency(self):                         # Создаётся свойство только для чтения типа валюты
        return self._currency   

    def __str__(self):
        return f"{self._whole}.{self._cents:02d} {self._currency}"

    def set_whole(self, value):     # определить набор_целиком(сам себя, значение) # Объявляется метод с именем set_whole(сэт хой), 
                                    # который принимает два параметра: self — ссылка на текущий объект (обязательно в методах класса)
                                    # value — новое значение целой части, которое хочет установить пользователь.
        if value < 0:               # проверяется условие: является ли переданное значение отрицательным.
# В контексте денег (если не предусмотрены долги или балансы) отрицательная целая часть обычно не имеет смысла. 
            raise ValueError("Whole part cannot be negative")
        # Если значение меньше нуля, программа прерывает выполнение и выдаёт исключение ValueError с понятным сообщением.
# Это предотвращает некорректное состояние объекта (например, -10 долларов 50 центов — если такая логика не поддерживается). 
        self._whole = value
       #  Если проверка пройдена (значение ≥ 0), то приватному атрибуту _whole присваивается новое значение.
       # Это безопасное обновление внутреннего состояния объекта. 

    def set_cents(self, value): 
        # Этот метод позволяет изменить дробную часть денег (копейки, центы и т.п.), с проверкой 
        # корректности — ведь в любой валюте дробная часть не может быть ≥ 100 (100 центов = 1 доллар).
        # Объявляется метод set_cents, принимающий: self — текущий объект. value — новое значение копеек/центов.
        if not (0 <= value < 100):
#             Проверяется, лежит ли значение в допустимом диапазоне: 
# минимум 0 (нельзя иметь «минус 10 копеек»),
# максимум 99 (100 копеек — это уже 1 целая единица валюты, и должна увеличить _whole, а не храниться в _cents).
# Оператор not инвертирует условие: если значение выходит за пределы [0, 99], сработает исключение.
            raise ValueError("Cents must be between 0 and 99") # Количество центов должно составлять от 0 до 99
#         Выбрасывается исключение с понятным сообщением, если значение некорректно.  
# > Это помогает программисту быстро понять ошибку при отладке.
        self._cents = value
#         Если значение в допустимом диапазоне — оно сохраняется в приватный атрибут _cents.
# Теперь объект хранит корректную дробную часть. 

if __name__ == "__main__":
    money1 = Money(50, 75, "USD")
    print(money1)  # → 50.75 USD

    money1.set_cents(30)
    print(money1)  # → 50.30 USD

    money1.set_whole(100)
    print(money1)  # → 100.30 USDss