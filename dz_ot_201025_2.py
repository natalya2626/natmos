# 2. Вы создаёте простую консольную программу-помощник для бытовых расчётов.
#  При запуске программа выводит меню из трёх пунктов:  
# 1) Рассчитать площадь прямоугольной комнаты  
# 2) Перевести скорость из км/ч в м/с  
# 3) Узнать, хватит ли заряда телефона до конца дня  

# Пользователь вводит номер пункта (1, 2 или 3). В зависимости от выбора 
# вызывается соответствующая функция, которая запрашивает необходимые данные 
# с помощью input() и выводит результат. Для этого реализуются четыре функции: 
# три расчётные и одна основная - main_menu().  

# Первая функция - calculate_room_area() - запрашивает длину и ширину комнаты 
# в метрах и выводит площадь в квадратных метрах;
# Вторая функция - convert_speed() - запрашивает скорость в км/ч и выводит 
# эквивалент в м/с; 
# Третья функция - check_battery_life() - запрашивает текущий заряд в процентах
# и оставшееся время активного использования в часах, затем проверяет, 
# хватит ли заряда, если телефон тратит 12% в час; выводит соответствующее сообщение.  
# Четвёртая функция - main_menu() - выводит меню, читает выбор пользователя и 
# вызывает одну из трёх функций. Если введён неверный номер, программа сообщает об ошибке.


def calculate_room_area():
    length = int(input("Введите длину (м): "))
    width = int(input("Введите ширину (м): "))
    area = length * width
    print(f"Площадь комнаты: {area} м2")
    
def convert_speed():
    speed_kmh = int(input("Введите скорость (км/ч): "))   
    speed_ms = speed_kmh / 3.6  
    print(f"Скорость: {speed_ms:.2f} м/с")                     

def check_battery_life():
    charge = int(input("Текущий заряд в(%): "))     
    hours_needed = int(input("Сколько часов вам нужно? ")) 
    if hours_needed <= charge / 12:
        print("Заряда хватит")
    else: 
        print("Заряда не хватит")    




def main_menu():   #  главное меню 
    print("Выберите что расчитать: ")
    print("1) Рассчитать площадь прямоугольной комнаты")
    print("2) Перевести скорость из км/ч в м/с ")
    print("3) Узнать, хватит ли заряда телефона до конца дня ")

    choice = input ("Ваш выбор (1, 2 или 3):  ")
    # Обработка выбора
    if choice  == "1":
        calculate_room_area()        
    elif choice == "2":    
        convert_speed()
    elif choice == "3":
        check_battery_life()
    else:
        print("Ошибка: нужно ввести 1, 2 или 3.")

if __name__ == "__main__": 
    main_menu()  